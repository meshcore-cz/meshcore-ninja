// Fetches the GitHub contributor list for the project repo and writes
// data/contributors.json (committed). Like enrich-firmwares.js, this is
// network-backed and intentionally does NOT run during build:data — refresh it
// explicitly or from a scheduled workflow. build-data.js folds the committed
// file into data.json; the homepage renders it.
//
// Usage:
//   npm run enrich:contributors
//   npm run enrich:contributors -- --dry-run
//
// Honors GITHUB_TOKEN to raise the GitHub API rate limit.
import { readFileSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import { downloadAvatars } from './download-avatars.js';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const outPath = join(root, 'data', 'contributors.json');
const dryRun = process.argv.slice(2).includes('--dry-run');

// Repo to read contributors from — parsed from REPO_URL in src/lib/seo.js so it
// tracks the configured project repository. GitHub follows the redirect if the
// repo has since been renamed.
function repoFromSeo() {
  const seo = readFileSync(join(root, 'src', 'lib', 'seo.js'), 'utf8');
  const m = /REPO_URL\s*=\s*['"]https?:\/\/github\.com\/([^/]+\/[^/'"#?]+)/.exec(seo);
  if (!m) throw new Error('Could not parse REPO_URL (owner/repo) from src/lib/seo.js');
  return m[1].replace(/\.git$/, '');
}

const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

// Fetches a GitHub API path. The /stats/* endpoints compute asynchronously and
// answer 202 while a cache warms up; retry a few times before giving up.
async function githubJson(path, { retries = 5 } = {}) {
  const headers = {
    Accept: 'application/vnd.github+json',
    'User-Agent': 'meshcore-ninja',
    'X-GitHub-Api-Version': '2022-11-28'
  };
  if (process.env.GITHUB_TOKEN) headers.Authorization = `Bearer ${process.env.GITHUB_TOKEN}`;

  for (let attempt = 0; ; attempt++) {
    const res = await fetch(`https://api.github.com${path}`, { headers });
    if (res.status === 403 && res.headers.get('x-ratelimit-remaining') === '0') {
      const reset = Number(res.headers.get('x-ratelimit-reset') ?? 0) * 1000;
      const resetAt = Number.isFinite(reset) && reset > 0 ? new Date(reset).toISOString() : 'unknown';
      throw new Error(`GitHub API rate limit reached; set GITHUB_TOKEN or retry after ${resetAt}`);
    }
    if (res.status === 202) {
      if (attempt >= retries) throw new Error(`GitHub still computing ${path} after ${retries} retries`);
      await sleep(2000 * (attempt + 1));
      continue;
    }
    if (!res.ok) throw new Error(`GitHub API ${res.status} for ${path}`);
    // 202 returns an empty body; a warmed cache returns the data.
    const text = await res.text();
    return text ? JSON.parse(text) : null;
  }
}

const isBot = (login, type) => type === 'Bot' || /\[bot\]$/i.test(login ?? '');

async function main() {
  const repo = repoFromSeo();
  // /stats/contributors is authoritative (per-author commit totals); the plain
  // /contributors endpoint is cache-backed and can omit recent contributors.
  const [raw, repoMeta] = await Promise.all([
    githubJson(`/repos/${repo}/stats/contributors`),
    githubJson(`/repos/${repo}`)
  ]);
  if (!Array.isArray(raw)) throw new Error(`Unexpected stats response for ${repo}`);

  const contributors = raw
    .filter((c) => c.author && !isBot(c.author.login, c.author.type))
    .sort((a, b) => (b.total ?? 0) - (a.total ?? 0))
    .map((c) => ({
      login: c.author.login,
      avatarUrl: c.author.avatar_url,
      htmlUrl: c.author.html_url,
      contributions: c.total
    }));

  const payload = {
    $comment: 'Generated by npm run enrich:contributors. Do not edit by hand.',
    repo,
    githubStars: repoMeta?.stargazers_count ?? null,
    lastChecked: new Date().toISOString().slice(0, 10),
    contributors
  };

  console.log(`${repo}: ${contributors.length} human contributor(s), ${payload.githubStars ?? '?'} star(s)`);
  if (dryRun) {
    console.log(JSON.stringify(payload, null, 2));
    return;
  }
  writeFileSync(outPath, JSON.stringify(payload, null, 2) + '\n');
  console.log(`✓ Wrote ${outPath}`);

  // Mirror the freshly-listed avatars locally (rewrites contributors.json with
  // local `avatar` paths and prunes departed contributors).
  console.log('Mirroring avatars…');
  await downloadAvatars(root);
}

main().catch((err) => {
  console.error(err.message);
  process.exit(1);
});
